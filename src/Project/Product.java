public Product(String productId, String category) {
        initComponents();

        // Save the category for the back button
        this.productCategory = category;

        // --- Login Button Visibility ---
        if (Loginnew.userIsLoggedIn) {
            jButton4.setVisible(false);
            jButton5.setVisible(false);
            btnProfile.setVisible(true);
        } else {
            jButton4.setVisible(true);
            jButton5.setVisible(true);
            btnProfile.setVisible(false);
        }

        // --- Main Product Details ---
        ProductData product = ProductDataBase.getProductById(productId);

        if (product != null) {
            // --- START OF THE FIX ---

            // 1. Get the image path from the product data you loaded.
            String imagePath = product.getImagePath();
            
            // 2. Save this path to your class variable so the cart button can use it.
            this.currentProductImagePath = imagePath; 

            // 3. Set the product details on the page.
            jLabel1.setText(product.getName());
            jLabel6.setText("฿ " + product.getPrice());
            setProductImage(jLabel3, imagePath); // Use the path we just got

            // --- END OF THE FIX ---

            // --- Related Products Section (This code is already correct) ---
            java.util.List<ProductData> relatedProducts = ProductDataBase.getProductsByCategory(product.getCategory(), product.getId());

            javax.swing.JLabel[] imageLabels = {jLabel4, jLabel11, jLabel14};
            javax.swing.JLabel[] nameLabels = {jLabel8, jLabel12, jLabel15};
            javax.swing.JLabel[] priceLabels = {jLabel7, jLabel9, jLabel13};
            javax.swing.JPanel[] panels = {jPanel4, jPanel5, jPanel6};

            for (javax.swing.JPanel panel : panels) {
                panel.setVisible(false);
            }

            for (int i = 0; i < relatedProducts.size(); i++) {
                if (i >= panels.length) {
                    break;
                }
                final ProductData related = relatedProducts.get(i);
                panels[i].setVisible(true);
                setProductImage(imageLabels[i], related.getImagePath());
                nameLabels[i].setText(related.getName());
                priceLabels[i].setText(related.getPrice() + "฿");
                imageLabels[i].addMouseListener(new java.awt.event.MouseAdapter() {
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        dispose();
                        new Product(related.getId(), related.getCategory()).setVisible(true);
                    }
                });
            }
        }
    }
