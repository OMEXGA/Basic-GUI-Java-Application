private void setImage(javax.swing.JLabel label, String imagePath) {
    if (label == null) {
        System.err.println("JLabel is null for path: " + imagePath);
        return;
    }

    java.net.URL imgUrl = getClass().getResource(imagePath);
    if (imgUrl != null) {
        javax.swing.ImageIcon icon = new javax.swing.ImageIcon(imgUrl);

        java.awt.Image scaledImage = icon.getImage().getScaledInstance(
            label.getWidth(),
            label.getHeight(),
            java.awt.Image.SCALE_SMOOTH
        );
        // ------------------------------------

        label.setIcon(new javax.swing.ImageIcon(scaledImage));

    } else {
        System.err.println("Image not found: " + imagePath);
    }
}

