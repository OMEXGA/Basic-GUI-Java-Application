    private void loadUserProfile() {
        String loggedInEmail = Project.Loginnew.loggedInUserEmail;

        if (loggedInEmail == null || loggedInEmail.isEmpty()) {
            jLabel5.setText("Email: Not logged in");
            jLabel4.setText("Date of Creation: Not available");
            return;
        }


        jLabel4.setText("Email: " + loggedInEmail);

        String creationDate = "Date not found"; 
        String filePath = "account.txt";

        try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] userData = line.split(","); 
                if (userData.length >= 3 && userData[0].equals(loggedInEmail)) {
                    creationDate = userData[2]; 
                    break; 
                }
            }
        } catch (IOException e) {
            creationDate = "Error reading file";
            e.printStackTrace();
        }

        jLabel5.setText("Date of Creation: " + creationDate);
    }

